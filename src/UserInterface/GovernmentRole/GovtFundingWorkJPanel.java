     public void populateFundRequestTable() {
        DefaultTableModel model = (DefaultTableModel) govtWorkRequestJTable.getModel();

        model.setRowCount(0);

        for(Network network: system.getNetworkList() )
        {
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList())
        {
            for(Organization o: enterprise.getOrganizationDirectory().getOrganizationList()){
      
        for (WorkRequest wr : o.getWorkQueue().getWorkRequestList()) {

            
                Object[] row = new Object[5];
                row[0]= wr.getIncome();
                row[1]=wr.getReceiver()==null?null:wr.getReceiver().getUsername();
                row[2]=wr.getStatus();
                String result = ((LoanApprovementWorkRequest)wr).getResult();
                row[3] = result==null?null:result;
                row[4]=wr.getLoanRequired();
                
                model.addRow(row);
            
        }
       }    
            }
        }
        
    }

    
    
    
