    private void RequestjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequestjButtonActionPerformed
        // TODO add your handling code here:
        LoanApprovementWorkRequest wr = new LoanApprovementWorkRequest();
        wr.setLoanRequired(fundingjTextField.getText());
        wr.setIncome(incomeJTextField.getText());
        wr.setStatus("Sent");
        
        Organization o = null;
        for(Network network: system.getNetworkList() )
        {
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList())
        {
            if(enterprise instanceof BankEnterprise){
        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList())
        {
        if(organization instanceof EmployeeOrganization )
        {
            o=organization;
            break;
        }
        
        }
            }
            if(o!= null)
            {
            o.getWorkQueue().getWorkRequestList().add(wr);
            }
        
        }
        
        }
        populateFundRequestTable();
        
        
    }//GEN-LAST:event_RequestjButtonActionPerformed

