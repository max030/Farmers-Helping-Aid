      public void populateEmployeeRequestTable(){
       DefaultTableModel model = (DefaultTableModel) employeeWorkRequestJTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : employeeOrganization.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            row[0] = request.getMessage();
            row[1] = request.getSender();
            row[2] = request.getReceiver();
            BankEmployeeWorkRequest result = ((BankEmployeeWorkRequest) request);
            
            row[3] = result;
           
            
            model.addRow(row);
        }
}
    
    
    
