    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

      try{
                int selectedRow = employeeWorkRequestJTable.getSelectedRow();
        
                if (selectedRow < 0){
                        return;
                    }
                BankEmployeeWorkRequest  request = ( BankEmployeeWorkRequest)employeeWorkRequestJTable.getValueAt(selectedRow, 3);
                request.setReceiver(account);
                request.setStatus("Pending");
                populateEmployeeRequestTable();
        }
        catch(Exception ex){
            System.out.println(ex);
            
        }
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = employeeWorkRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }
                BankEmployeeWorkRequest request = (BankEmployeeWorkRequest)employeeWorkRequestJTable.getValueAt(selectedRow, 3);
                request.setStatus("Processing");
                ProcessRequestEmployeeJPanel processRequestEmployeeJPanel = new ProcessRequestEmployeeJPanel(userProcessContainer, account,(BankEmployeeWorkRequest)request);
                userProcessContainer.add("processRequestEmployeeJPanel", processRequestEmployeeJPanel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
    }//GEN-LAST:event_processJButtonActionPerformed


